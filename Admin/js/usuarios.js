function fnFormatDetails(oTable, nTr) {
  var aData = oTable.fnGetData(nTr);
  var sOut =
    '<table cellpadding="5" cellspacing="0" border="3" style="padding-left:50px;">';
  sOut +=
    "<tr><td>Correo:</td><td>" +
    aData[2] +
    "</td><td>Cargo:</td><td>" +
    aData[3] +
    "</td><td>Rol:</td><td>" +
    aData[4] +
    "</td><td>Teléfono:</td><td>" +
    aData[5] +
    "</td></tr>";
  sOut += "</table>";

  return sOut;
}
